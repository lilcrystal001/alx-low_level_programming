/**
 * *_memset- this fills memory with a constant amount of byte
 * @s: memory section to be filled
 * @b: char to copy
 * @n: number of times b is to be copied
 *
 * Return: Pointer to the memory address of s
 *
 */

char *_memset(char *s, char b, unsigned int s)
{
	unsigned int i;

	for (i = 0; i < n; i++)

	{
		s[i] = b;
	}

	return (s);
}
